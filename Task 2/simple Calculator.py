#simple calculator
number1=int(input("Enter ur first number :"))
number2=int(input("Enter ur Second number :"))
operation=input("Enter the symbol of ur Operation :")
if operation=="+":
    print( "The answer for the Addition operation is :",number1+number2)
if operation=="-":
    print( "The answer for the Subtraction operation is :",number1-number2)
if operation=="*":
    print( "The answer for the  Multiplication operation is :",number1*number2)
if operation=="/":
    print( "The answer for the Division operation is :",number1/number2)
